#!/bin/bash
# Save the HTML report to a specific file

BOOK_PATH="$1"
OUTPUT_PATH="${2:-report.html}"

if [ -z "$BOOK_PATH" ]; then
    echo "Usage: $0 <path-to-book.pdf> [output.html]"
    echo "Example: $0 ~/Downloads/MyBook.pdf my_report.html"
    exit 1
fi

echo "Processing book and generating HTML report..."

# Run workflow and get response
RESPONSE=$(./scripts/upload_text.sh "$BOOK_PATH")

# Extract report fields
HTML_REPORT=$(echo "$RESPONSE" | jq -r '.report_html')
BOOK_TITLE=$(echo "$RESPONSE" | jq -r '.book_title')
TOP_GENRE=$(echo "$RESPONSE" | jq -r '.top_genre')

if [ -z "$HTML_REPORT" ] || [ "$HTML_REPORT" = "null" ]; then
    echo "Error: No HTML report in response"
    exit 1
fi

# Create styled HTML file
